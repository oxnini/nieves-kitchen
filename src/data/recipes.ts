// Helper: get all unique countries that have recipes
export function getRecipeCountries(): string[] {
  return [...new Set(recipes.map(r => r.country))];
}

// Helper: get recipes for a specific country
export function getRecipesByCountry(country: string): Recipe[] {
  return recipes.filter(r => r.country === country);
}

// Helper: get recipes for a specific region
export function getRecipesByRegion(region: string): Recipe[] {
  return recipes.filter(r => r.region === region);
}
